import { useState } from 'react'
import { Link, useNavigate } from 'react-router-dom'
import { useAuth } from '../../hooks/useAuth'

export default function Signup() {
  const [fullName, setFullName] = useState('')
  const [email, setEmail] = useState('')
  const [password, setPassword] = useState('')
  const [error, setError] = useState('')
  const [loading, setLoading] = useState(false)
  const { signUp, signInWithGoogle } = useAuth()
  const navigate = useNavigate()

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault()
    setError('')
    setLoading(true)
    const { error } = await signUp(email, password, fullName)
    if (error) { setError(error.message || 'Erreur lors de la création du compte.'); setLoading(false) }
    else { navigate('/dashboard') }
  }

  const handleGoogle = async () => {
    const { error } = await signInWithGoogle()
    if (error) setError('Erreur lors de la connexion avec Google.')
  }

  return (
    <div className="min-h-screen bg-gray-50 flex items-center justify-center px-6 py-12">
      <div className="w-full max-w-sm">
        <div className="text-center mb-8"><Link to="/" className="text-xl font-semibold text-gray-900 tracking-tight">VotreSaaS</Link></div>
        <div className="bg-white border border-gray-200 rounded-xl p-8">
          <h1 className="text-xl font-semibold text-gray-900 text-center">Créez votre compte</h1>
          <p className="text-sm text-gray-500 text-center mt-1">Commencez votre essai gratuit de 14 jours</p>
          <button onClick={handleGoogle} className="mt-6 w-full flex items-center justify-center gap-3 border border-gray-200 rounded-lg py-2.5 text-sm text-gray-700 hover:bg-gray-50 transition-colors">
            <svg width="18" height="18" viewBox="0 0 24 24"><path d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 01-2.2 3.32v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.1z" fill="#4285F4"/><path d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" fill="#34A853"/><path d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" fill="#FBBC05"/><path d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" fill="#EA4335"/></svg>
            Continuer avec Google
          </button>
          <div className="flex items-center gap-3 my-6"><div className="flex-1 h-px bg-gray-200"></div><span className="text-xs text-gray-400">ou</span><div className="flex-1 h-px bg-gray-200"></div></div>
          <form onSubmit={handleSubmit} className="space-y-4">
            {error && <div className="bg-red-50 border border-red-100 text-red-600 text-xs rounded-lg px-4 py-3">{error}</div>}
            <div><label className="block text-xs font-medium text-gray-700 mb-1.5">Nom complet</label><input type="text" value={fullName} onChange={(e) => setFullName(e.target.value)} placeholder="Jean Dupont" required className="w-full border border-gray-200 rounded-lg px-3 py-2.5 text-sm text-gray-900 placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-indigo-600/20 focus:border-indigo-600 transition-colors" /></div>
            <div><label className="block text-xs font-medium text-gray-700 mb-1.5">Email professionnel</label><input type="email" value={email} onChange={(e) => setEmail(e.target.value)} placeholder="vous@entreprise.com" required className="w-full border border-gray-200 rounded-lg px-3 py-2.5 text-sm text-gray-900 placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-indigo-600/20 focus:border-indigo-600 transition-colors" /></div>
            <div><label className="block text-xs font-medium text-gray-700 mb-1.5">Mot de passe</label><input type="password" value={password} onChange={(e) => setPassword(e.target.value)} placeholder="Minimum 8 caractères" required minLength={8} className="w-full border border-gray-200 rounded-lg px-3 py-2.5 text-sm text-gray-900 placeholder-gray-400 focus:outline-none focus:ring-2 focus:ring-indigo-600/20 focus:border-indigo-600 transition-colors" /></div>
            <button type="submit" disabled={loading} className="w-full bg-gray-900 text-white py-2.5 rounded-lg text-sm font-medium hover:bg-gray-800 disabled:opacity-50 transition-colors">{loading ? 'Création...' : 'Créer mon compte'}</button>
          </form>
          <p className="text-xs text-gray-400 text-center mt-4">En créant un compte, vous acceptez nos <a href="#" className="text-indigo-600 hover:underline">CGV</a> et notre <a href="#" className="text-indigo-600 hover:underline">politique de confidentialité</a>.</p>
        </div>
        <p className="text-center text-sm text-gray-500 mt-6">Déjà un compte ? <Link to="/login" className="text-indigo-600 hover:text-indigo-700 font-medium">Se connecter</Link></p>
      </div>
    </div>
  )
}
